package Chapter5;

public class EX02 {

	public static void main(String[] args) {
		// 문자열을 반복문 횟수를 역순으로 하여 10번 출력
		for(int i = 10; i > 0; i--) {
			System.out.println("JAVA STRING!");
		}	

		System.out.println();
		
		// 문자열을 반복문 횟수를 역순으로 하여 10번 순서 확인 및 출력
		for(int i = 10; i > 0; i--) {
			System.out.println(i + "번 JAVA STRING!");
		}
		
	}
}
