package Chapter5;

public class EX03 {

	public static void main(String[] args) {
		// 0 - 10 까지 한 줄로 출력
		for(int i = 0; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		// 5 - 10 까지 한 줄로 출력
		for(int i = 5; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		// 5 - 10 + "안녕" 한 줄로 출력
		for(int i = 5; i <= 10; i++) {
			System.out.print(i + "안녕 ");
		}
	}

}
