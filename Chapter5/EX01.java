package Chapter5;

public class EX01 {

	public static void main(String[] args) {
		// 문자열 10번 출력
		for(int i = 0; i < 10; i++) {
			System.out.println("JAVA STRING!");
		}
		
		System.out.println();
		
		// 숫자 10까지 출력
		for(int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
	}

}
